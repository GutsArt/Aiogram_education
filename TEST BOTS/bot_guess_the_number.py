import logging
from aiogram import Bot, Dispatcher, F
from aiogram.filters import Command, CommandStart
from aiogram.types import Message

from config import BOT_TOKEN

logging.basicConfig(
    level=logging.INFO,  # INFO –ø–æ–∫–∞–∂–µ—Ç –≤—Å—ë –≤–∞–∂–Ω–æ–µ; DEBUG ‚Äî –¥–ª—è –¥–µ—Ç–∞–ª—å–Ω–æ–π –æ—Ç–ª–∞–¥–∫–∏
    format="%(asctime)s [%(levelname)s] %(message)s",
    handlers=[
        logging.StreamHandler(),                # –í—ã–≤–æ–¥ –≤ –∫–æ–Ω—Å–æ–ª—å
        logging.FileHandler("bot.log", encoding="utf-8")  # –ó–∞–ø–∏—Å—å –≤ —Ñ–∞–π–ª bot.log
    ]
)

bot = Bot(token=BOT_TOKEN)
dp = Dispatcher()

ATTEMPTS = 5

from collections import defaultdict

users: dict = defaultdict(lambda: {
    'in_game': False,
    'secret_number': None,
    'attempts': None,
    'total_games': 0,
    'wins': 0
})
# logging.info(f"üë§ –ù–æ–≤—ã–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å: {message.from_user.id} ({message.from_user.full_name})")


def get_random_number() -> int:
    import random
    return random.randint(1, 100)


# –≠—Ç–æ—Ç —Ö—ç–Ω–¥–ª–µ—Ä –±—É–¥–µ—Ç —Å—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å –Ω–∞ –∫–æ–º–∞–Ω–¥—É "/start"
@dp.message(CommandStart()) # or Command(commands='start')
async def process_start_command(message: Message):
    await message.answer(
        '–ü—Ä–∏–≤–µ—Ç!\n–î–∞–≤–∞–π—Ç–µ —Å—ã–≥—Ä–∞–µ–º –≤ –∏–≥—Ä—É "–£–≥–∞–¥–∞–π —á–∏—Å–ª–æ"?\n\n'
        '–ß—Ç–æ–±—ã –ø–æ–ª—É—á–∏—Ç—å –ø—Ä–∞–≤–∏–ª–∞ –∏–≥—Ä—ã –∏ —Å–ø–∏—Å–æ–∫ –¥–æ—Å—Ç—É–ø–Ω—ã—Ö '
        '–∫–æ–º–∞–Ω–¥ - –æ—Ç–ø—Ä–∞–≤—å—Ç–µ –∫–æ–º–∞–Ω–¥—É /help'
    )


# –≠—Ç–æ—Ç —Ö—ç–Ω–¥–ª–µ—Ä –±—É–¥–µ—Ç —Å—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å –Ω–∞ –∫–æ–º–∞–Ω–¥—É "/help"
@dp.message(Command(commands='help'))
async def process_help_command(message: Message):
    await message.answer(
        '–ü—Ä–∞–≤–∏–ª–∞ –∏–≥—Ä—ã:\n\n'
        '–Ø –∑–∞–≥–∞–¥—ã–≤–∞—é —á–∏—Å–ª–æ –æ—Ç 1 –¥–æ 100, –∞ –≤–∞–º –Ω—É–∂–Ω–æ –µ–≥–æ —É–≥–∞–¥–∞—Ç—å\n' 
        f'–£ –≤–∞—Å –µ—Å—Ç—å {ATTEMPTS} –ø–æ–ø—ã—Ç–æ–∫\n\n'
        '–î–æ—Å—Ç—É–ø–Ω—ã–µ –∫–æ–º–∞–Ω–¥—ã:\n'
        '/help - –ø—Ä–∞–≤–∏–ª–∞ –∏–≥—Ä—ã –∏ —Å–ø–∏—Å–æ–∫ –∫–æ–º–∞–Ω–¥\n'
        '/cancel - –≤—ã–π—Ç–∏ –∏–∑ –∏–≥—Ä—ã\n'
        '/stat - –ø–æ—Å–º–æ—Ç—Ä–µ—Ç—å —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É\n\n'
        '–î–∞–≤–∞–π —Å—ã–≥—Ä–∞–µ–º?'
    )


# –≠—Ç–æ—Ç —Ö—ç–Ω–¥–ª–µ—Ä –±—É–¥–µ—Ç —Å—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å –Ω–∞ –∫–æ–º–∞–Ω–¥—É "/stat"
@dp.message(Command(commands='stat'))
async def process_stat_command(message: Message):
    await message.answer(
        '–í—Å–µ–≥–æ –∏–≥—Ä —Å—ã–≥—Ä–∞–Ω–æ: '
        f'{users[message.from_user.id]["total_games"]}\n'
        f'–ò–≥—Ä –≤—ã–∏–≥—Ä–∞–Ω–æ: {users[message.from_user.id]["wins"]}'
    )


# –≠—Ç–æ—Ç —Ö—ç–Ω–¥–ª–µ—Ä –±—É–¥–µ—Ç —Å—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å –Ω–∞ –∫–æ–º–∞–Ω–¥—É "/cancel"
@dp.message(Command(commands='cancel'))
async def process_cancel_command(message: Message):
    if users[message.from_user.id]['in_game']:
        users[message.from_user.id]['in_game'] = False
        # logging.info(f"‚ùå {message.from_user.id} –≤—ã—à–µ–ª –∏–∑ –∏–≥—Ä—ã")
        await message.answer(
            '–í—ã –≤—ã—à–ª–∏ –∏–∑ –∏–≥—Ä—ã. –ï—Å–ª–∏ –∑–∞—Ö–æ—Ç–∏—Ç–µ —Å—ã–≥—Ä–∞—Ç—å '
            '—Å–Ω–æ–≤–∞ - –Ω–∞–ø–∏—à–∏—Ç–µ –æ–± —ç—Ç–æ–º'
        )
    else:
        await message.answer(
            '–ê –º—ã –∏ —Ç–∞–∫ —Å –≤–∞–º–∏ –Ω–µ –∏–≥—Ä–∞–µ–º. '
            '–ú–æ–∂–µ—Ç, —Å—ã–≥—Ä–∞–µ–º —Ä–∞–∑–æ–∫?'
        )


# –≠—Ç–æ—Ç —Ö—ç–Ω–¥–ª–µ—Ä –±—É–¥–µ—Ç —Å—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å –Ω–∞ —Å–æ–≥–ª–∞—Å–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å—ã–≥—Ä–∞—Ç—å –≤ –∏–≥—Ä—É
@dp.message(F.text.lower().in_(['–¥–∞', '–¥–∞–≤–∞–π', '–∏–≥—Ä–∞',
                                '–∏–≥—Ä–∞—Ç—å', '—Ö–æ—á—É', 'yes', 'y', 'ok']))
async def process_positive_answer(message: Message):
    if not users[message.from_user.id]['in_game']:
        users[message.from_user.id]['in_game'] = True
        users[message.from_user.id]['secret_number'] = get_random_number()
        users[message.from_user.id]['attempts'] = ATTEMPTS
        # logging.info(f"üéÆ {message.from_user.id} –Ω–∞—á–∞–ª –∏–≥—Ä—É. –ó–∞–≥–∞–¥–∞–Ω–æ: {users[message.from_user.id]['secret_number']}")

        await message.answer(
            '–£—Ä–∞!\n\n–Ø –∑–∞–≥–∞–¥–∞–ª —á–∏—Å–ª–æ –æ—Ç 1 –¥–æ 100, '
            '–ø–æ–ø—Ä–æ–±—É–π —É–≥–∞–¥–∞—Ç—å!'
        )
    else:
        await message.answer(
            '–ü–æ–∫–∞ –º—ã –∏–≥—Ä–∞–µ–º –≤ –∏–≥—Ä—É —è –º–æ–≥—É '
            '—Ä–µ–∞–≥–∏—Ä–æ–≤–∞—Ç—å —Ç–æ–ª—å–∫–æ –Ω–∞ —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ 100 '
            '–∏ –∫–æ–º–∞–Ω–¥—ã /cancel –∏ /stat'
        )


# –≠—Ç–æ—Ç —Ö—ç–Ω–¥–ª–µ—Ä –±—É–¥–µ—Ç —Å—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å –Ω–∞ –æ—Ç–∫–∞–∑ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å—ã–≥—Ä–∞—Ç—å –≤ –∏–≥—Ä—É
@dp.message(F.text.lower().in_(['–Ω–µ—Ç', '–Ω–µ', '–Ω–µ —Ö–æ—á—É', 'no', 'n']))
async def process_negative_answer(message: Message):
    if not users[message.from_user.id]['in_game']:
        # logging.info(f"üö´ {message.from_user.id} –æ—Ç–∫–∞–∑–∞–ª—Å—è –∏–≥—Ä–∞—Ç—å")
        await message.answer(
            '–ñ–∞–ª—å :(\n\n' \
            '–ï—Å–ª–∏ –∑–∞—Ö–æ—Ç–∏—Ç–µ –ø–æ–∏–≥—Ä–∞—Ç—å - –ø—Ä–æ—Å—Ç–æ –Ω–∞–ø–∏—à–∏—Ç–µ –æ–± —ç—Ç–æ–º'
        )
    else:
        await message.answer(
            '–ú—ã –∂–µ —Å–µ–π—á–∞—Å —Å –≤–∞–º–∏ –∏–≥—Ä–∞–µ–º. –ü—Ä–∏—Å—ã–ª–∞–π—Ç–µ, –ø–æ–∂–∞–ª—É–π—Å—Ç–∞, —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ 100'
        )


# –≠—Ç–æ—Ç —Ö—ç–Ω–¥–ª–µ—Ä –±—É–¥–µ—Ç —Å—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å –Ω–∞ –æ—Ç–ø—Ä–∞–≤–∫—É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º —á–∏—Å–µ–ª –æ—Ç 1 –¥–æ 100
@dp.message(lambda x: x.text and x.text.isdigit() and 1 <= int(x.text) <= 100)
async def process_numbers_answer(message: Message):
    if users[message.from_user.id]['in_game']:
        if int(message.text) == users[message.from_user.id]['secret_number']:
            users[message.from_user.id]['in_game'] = False
            users[message.from_user.id]['total_games'] += 1
            users[message.from_user.id]['wins'] += 1
            await message.answer(
                '–£—Ä–∞!!! –í—ã —É–≥–∞–¥–∞–ª–∏ —á–∏—Å–ª–æ!\n\n'
                '–ú–æ–∂–µ—Ç, —Å—ã–≥—Ä–∞–µ–º –µ—â–µ?'
            )
        elif int(message.text) > users[message.from_user.id]['secret_number']:
            users[message.from_user.id]['attempts'] -= 1
            await message.answer('–ú–æ–µ —á–∏—Å–ª–æ –º–µ–Ω—å—à–µ')
        elif int(message.text) < users[message.from_user.id]['secret_number']:
            users[message.from_user.id]['attempts'] -= 1
            await message.answer('–ú–æ–µ —á–∏—Å–ª–æ –±–æ–ª—å—à–µ')

        if users[message.from_user.id]['attempts'] == 0:
            users[message.from_user.id]['in_game'] = False
            users[message.from_user.id]['total_games'] += 1
            await message.answer(
                '–ö —Å–æ–∂–∞–ª–µ–Ω–∏—é, —É –≤–∞—Å –±–æ–ª—å—à–µ –Ω–µ –æ—Å—Ç–∞–ª–æ—Å—å –ø–æ–ø—ã—Ç–æ–∫. –í—ã –ø—Ä–æ–∏–≥—Ä–∞–ª–∏ :(\n\n'
                f'–ú–æ–µ —á–∏—Å–ª–æ –±—ã–ª–æ {users[message.from_user.id]["secret_number"]}\n\n'
                '–î–∞–≤–∞–π—Ç–µ —Å—ã–≥—Ä–∞–µ–º –µ—â–µ?'
            )
    else:
        await message.answer('–ú—ã –µ—â–µ –Ω–µ –∏–≥—Ä–∞–µ–º. –•–æ—Ç–∏—Ç–µ —Å—ã–≥—Ä–∞—Ç—å?')


# –≠—Ç–æ—Ç —Ö—ç–Ω–¥–ª–µ—Ä –±—É–¥–µ—Ç —Å—Ä–∞–±–∞—Ç—ã–≤–∞—Ç—å –Ω–∞ –æ—Å—Ç–∞–ª—å–Ω—ã–µ –ª—é–±—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è
@dp.message()
async def process_other_answers(message: Message):
    if users[message.from_user.id]['in_game']:
        await message.answer(
            '–ú—ã –∂–µ —Å–µ–π—á–∞—Å —Å –≤–∞–º–∏ –∏–≥—Ä–∞–µ–º. –ü—Ä–∏—Å—ã–ª–∞–π—Ç–µ, –ø–æ–∂–∞–ª—É–π—Å—Ç–∞, —á–∏—Å–ª–∞ –æ—Ç 1 –¥–æ 100'
        )
    else:
        await message.answer(
            '–Ø –¥–æ–≤–æ–ª—å–Ω–æ –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–Ω—ã–π –±–æ—Ç, –¥–∞–≤–∞–π—Ç–µ –ø—Ä–æ—Å—Ç–æ —Å—ã–≥—Ä–∞–µ–º –≤ –∏–≥—Ä—É?'
        )


if __name__ == '__main__':
    dp.run_polling(bot)
# –£–ª—É—á—à–µ–Ω–∏—è, –∫–æ—Ç–æ—Ä—ã–µ –º–æ–∂–Ω–æ –≤–Ω–µ—Å—Ç–∏ –≤ —ç—Ç–æ–≥–æ –±–æ—Ç–∞

# –ü—Ä–∏ –ø–µ—Ä–µ–∑–∞–ø—É—Å–∫–µ –±–æ—Ç–∞ —Å–ª–æ–≤–∞—Ä—å users –æ–±–Ω—É–ª—è–µ—Ç—Å—è –∏ –≤—Å–µ —Å–æ—Å—Ç–æ—è–Ω–∏—è –≤—Å–µ—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π, —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ, —Å–±—Ä–∞—Å—ã–≤–∞—é—Ç—Å—è. –°–∞–º–æ–µ –ø—Ä–æ—Å—Ç–æ–µ —Ä–µ—à–µ–Ω–∏–µ —Ç–∞–∫–æ–π –ø—Ä–æ–±–ª–µ–º—ã - –ø–µ—Ä–∏–æ–¥–∏—á–µ—Å–∫–∏ —Å–æ—Ö—Ä–∞–Ω—è—Ç—å —Å–ª–æ–≤–∞—Ä—å –≤ —Ñ–∞–π–ª, –∞ –ø–æ—Ç–æ–º, –ø—Ä–∏ –ø–µ—Ä–µ–∑–∞–ø—É—Å–∫–µ –±–æ—Ç–∞ —Å—á–∏—Ç—ã–≤–∞—Ç—å –µ–≥–æ –∏–∑ —Ñ–∞–π–ª–∞. –î–ª—è —ç—Ç–æ–≥–æ –ø–æ–¥–æ–π–¥—ë—Ç, –Ω–∞–ø—Ä–∏–º–µ—Ä, –±–∏–±–ª–∏–æ—Ç–µ–∫–∞ pickle. –ù–æ –≤–æ–æ–±—â–µ, —Ç–∞–∫–æ–µ —Ä–µ—à–µ–Ω–∏–µ –Ω–µ –æ—á–µ–Ω—å —Ö–æ—Ä–æ—à–µ–µ. –õ—É—á—à–µ - –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Å–æ—Å—Ç–æ—è–Ω–∏–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –±–∞–∑—É –¥–∞–Ω–Ω—ã—Ö –∏–ª–∏ FSMContext –Ω–∞ –±–∞–∑–µ –ø–µ—Ä—Å–∏—Å—Ç–µ–Ω—Ç–Ω–æ–≥–æ —Ö—Ä–∞–Ω–∏–ª–∏—â–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä, Redis). –û–± —ç—Ç–æ–º –º—ã –±—É–¥–µ–º –≥–æ–≤–æ—Ä–∏—Ç—å –≤ –¥—Ä—É–≥–∏—Ö —É—Ä–æ–∫–∞—Ö.

# –ò–Ω—Ç–µ—Ä—Ñ–µ–π—Å –≤–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏—è —Å –±–æ—Ç–æ–º –¥–æ–≤–æ–ª—å–Ω–æ —Å–∫—É—á–Ω—ã–π. –í–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏–µ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç —Ç–æ–ª—å–∫–æ —Å –ø–æ–º–æ—â—å—é –∫–æ–º–∞–Ω–¥ –∏ —Ç–µ–∫—Å—Ç–æ–≤—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π. –ú–æ–∂–Ω–æ –±—ã–ª–æ –±—ã –¥–æ–±–∞–≤–∏—Ç—å –∫–Ω–æ–ø–æ–∫, —Å—Ç–∏–∫–µ—Ä–æ–≤, –≤–æ–∑–º–æ–∂–Ω–æ, –¥–∞–∂–µ –∫–∞–∫–∏—Ö-–Ω–∏–±—É–¥—å –∫–∞—Ä—Ç–∏–Ω–æ–∫ –≤ –Ω–∞–≥—Ä–∞–¥—É –∑–∞ –ø–æ–±–µ–¥—É –∏ —Ç–∞–∫ –¥–∞–ª–µ–µ. –î–µ–π—Å—Ç–≤–∏—Ç–µ–ª—å–Ω–æ, —É–ª—É—á—à–∏—Ç—å —é–∑–µ—Ä-–∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å –Ω–µ –ø—Ä–æ–±–ª–µ–º–∞ –∏ –Ω–∞–≤—ã–∫–∏ –∏–∑ —Å–ª–µ–¥—É—é—â–∏—Ö —É—Ä–æ–∫–æ–≤ –ø–æ–º–æ–≥—É—Ç –≤ —ç—Ç–æ–º, –µ—Å–ª–∏ –≤—ã –∑–∞—Ö–æ—Ç–∏—Ç–µ —Å–¥–µ–ª–∞—Ç—å —ç—Ç–æ–≥–æ –±–æ—Ç–∞ –ø–æ–≤–µ—Å–µ–ª–µ–µ.

